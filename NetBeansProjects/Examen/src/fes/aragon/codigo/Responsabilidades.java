public abstract class Responsabilidades {

    private int tipo;

    public Responsabilidades(int tipo) {
        this.tipo = tipo;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public abstract String hacer();
}
