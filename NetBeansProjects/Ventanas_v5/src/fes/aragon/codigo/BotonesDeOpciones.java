    private void eventoCambio(ItemEvent evt) {
        if (jRadioButton1.isSelected()) {
            jTextField1.setText(Integer.toString(numUsuario));
        } else if (jRadioButton2.isSelected()) {
            jTextField1.setText(Integer.toOctalString(numUsuario));
        } else {
            jTextField1.setText(Integer.toHexString(numUsuario));
        }
        jTextField1.requestFocus();
    }

