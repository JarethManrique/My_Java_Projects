    private void convertir() {
        String texto = jTextField1.getText();
        if (jCheckBox1.isSelected()) {
            jTextField1.setText(texto.toUpperCase());
        } else {
            jTextField1.setText(texto.toLowerCase());
        }
        jTextField1.requestFocusInWindow();
    }

    private void quitarA() {
        String texto = jTextField1.getText();
        if (jCheckBox2.isSelected()) {
            this.temporal=jTextField1.getText();
            jTextField1.setText(texto.replace("a", "A"));
        } else {
            jTextField1.setText(temporal);
        }
        jTextField1.requestFocusInWindow();
    }

