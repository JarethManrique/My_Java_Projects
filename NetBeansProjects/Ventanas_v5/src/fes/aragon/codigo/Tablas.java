    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        Archivos objetoArchivo;
        JFileChooser directorio = new JFileChooser();
        directorio.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int seleccion = directorio.showSaveDialog(jPanel1);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            try {
                File archivo = directorio.getSelectedFile();
                objetoArchivo = new Archivos(archivo, this.tabla.getDatos());
                objetoArchivo.almacenar();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, "Error al guardar los datos");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        Archivos objetoArchivo;
        JFileChooser directorio = new JFileChooser();
        directorio.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int seleccion = directorio.showOpenDialog(jPanel1);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            try {
                File archivo = directorio.getSelectedFile();
                objetoArchivo = new Archivos(archivo, this.tabla.getDatos());
                this.tabla.setDatos(objetoArchivo.cargar());
                this.tabla.fireTableDataChanged();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, "Error al cargar los datos");
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(rootPane, "Error al cargar los datos");
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

